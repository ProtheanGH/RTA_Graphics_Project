#include "Component.h"
#include "Object.h"



//////////////////////////////////////////////////////////////////
// DEFAULTS
//////////////////////////////////////////////////////////////////
Component::Component(Object* _parent)
{
	m_parent = _parent;
}

Component::Component(const Component& _copy)
{
	*this = _copy;
}

Component& Component::operator = (const Component& _assign)
{
	if (this != &_assign)
	{
		if (m_parent)
		{
			delete m_parent;
			m_parent = nullptr;
		}
		m_parent = Object::Create();
		m_parent = _assign.m_parent;
	}
	return *this;
}

/*virtual*/Component::~Component()
{
	Destroy();
}


//////////////////////////////////////////////////////////////////
// ACCESSORS
//////////////////////////////////////////////////////////////////
Object* Component::GetParent() const
{
	return m_parent;
}



//////////////////////////////////////////////////////////////////
// MUTATORS
//////////////////////////////////////////////////////////////////
void Component::SetParent(Object* _parent)
{
	m_parent = _parent;
}



//////////////////////////////////////////////////////////////////
// INTERFACE
//////////////////////////////////////////////////////////////////
/*virtual*/ bool Component::Update()
{
	return true;
}

/*virtual*/ void Component::Initialize(Object* _parent)
{
	SetParent(_parent);
}

/*virtual*/ void Component::Destroy()
{
	
}

